<template>
  <div>
    <!-- watch侦听器: 作用可以侦听data/computed属性值改变, 当侦听的属性发生变化的时候，会触发对应的处理函数 
    语法：
    watch: {
      变量名 (newVal, oldVal){
        // 变量名对应值改变这里自动触发
      }
    }-->
    <input type="text" v-model="name" />
    <button @click="msg.count++">clickme--{{msg.count}}</button>
  </div>
</template>

<script>
export default {
  name: "VueTest",
  data() {
    return {
      name: "",
      msg:{
        count:1,
        obj:{
          count:1
        }
      }
    };
  },
  watch:{
    msg:{
      deep:true,
      handler(val,vval){
        console.log(val);
        console.log(vval);
      }
    }
  }
};
</script>